/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  LL_APB4_GRP1_EnableClock(LL_APB4_GRP1_PERIPH_SYSCFG);

  /* System interrupt init*/
  NVIC_SetPriorityGrouping(NVIC_PRIORITYGROUP_4);

  /* SysTick_IRQn interrupt configuration */
  NVIC_SetPriority(SysTick_IRQn, NVIC_EncodePriority(NVIC_GetPriorityGrouping(),15, 0));

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

/* Configure the peripherals common clocks */
  PeriphCommonClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_SPI1_Init();
  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

  uint8_t rxbuff[256];

  LL_DMA_SetPeriphAddress(DMA1, LL_DMA_STREAM_0, LL_SPI_DMA_GetRxRegAddr(SPI1)); // 设置外设地址
  LL_DMA_SetMemoryAddress(DMA1, LL_DMA_STREAM_0, (uint32_t)rxbuff); // 设备内存地址
  LL_DMA_SetDataLength(DMA1, LL_DMA_STREAM_0, 256); // 设置传输长度

  LL_DMA_EnableIT_TC(DMA1, LL_DMA_STREAM_0); // 开启传输完成中断
  LL_DMA_EnableIT_TE(DMA1, LL_DMA_STREAM_0); // 开启传输错误中断
//  LL_DMA_EnableStream(DMA1, LL_DMA_STREAM_0);

  uint8_t txbuff[256];
  for(int i=0; i<256; i++)
  {
	  txbuff[i] = i;
  }

  LL_DMA_SetPeriphAddress(DMA1, LL_DMA_STREAM_1, LL_SPI_DMA_GetTxRegAddr(SPI1)); // 设置外设地址
  LL_DMA_SetMemoryAddress(DMA1, LL_DMA_STREAM_1, (uint32_t)txbuff); // 设备内存地址
  LL_DMA_SetDataLength(DMA1, LL_DMA_STREAM_1, 256); // 设置传输长度

  LL_DMA_EnableIT_TC(DMA1, LL_DMA_STREAM_1); // 开启传输完成中断
  LL_DMA_EnableIT_TE(DMA1, LL_DMA_STREAM_1); // 开启传输错误中断
//  LL_DMA_EnableStream(DMA1, LL_DMA_STREAM_1);

  LL_SPI_EnableDMAReq_RX(SPI1); // 使能SPI DMA RX请求
  LL_SPI_EnableDMAReq_TX(SPI1); // 使能SPI DMA TX请求

  LL_SPI_Enable(SPI1); // 使能SPI
  LL_SPI_StartMasterTransfer(SPI1); // 启动SPI主机传输

  while (1)
  {

	  while(onRx | onTx); // 等待可以开启接收发送

	  LL_GPIO_ResetOutputPin(GPIOA, LL_GPIO_PIN_4); // 拉低片选信号

	  onRx = 1;
	  LL_DMA_EnableStream(DMA1, LL_DMA_STREAM_0); // 启动SPI DMA接收
	  onTx = 1;
	  LL_DMA_EnableStream(DMA1, LL_DMA_STREAM_1); // 启动SPI DMA发送

	  while(onRx | onTx); // 等待接收发送完成

	  LL_GPIO_SetOutputPin(GPIOA, LL_GPIO_PIN_4); // 拉高片选信号

	  LL_mDelay(1);

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

/**
  * @brief System Clock Configuration
  * @retval None
  */
void SystemClock_Config(void)
{
  LL_FLASH_SetLatency(LL_FLASH_LATENCY_2);
  while(LL_FLASH_GetLatency()!= LL_FLASH_LATENCY_2)
  {
  }
  LL_PWR_ConfigSupply(LL_PWR_LDO_SUPPLY);
  LL_PWR_SetRegulVoltageScaling(LL_PWR_REGU_VOLTAGE_SCALE2);
  while (LL_PWR_IsActiveFlag_VOS() == 0)
  {
  }
  LL_RCC_HSE_Enable();

   /* Wait till HSE is ready */
  while(LL_RCC_HSE_IsReady() != 1)
  {

  }
  LL_RCC_PLL_SetSource(LL_RCC_PLLSOURCE_HSE);
  LL_RCC_PLL1P_Enable();
  LL_RCC_PLL1R_Enable();
  LL_RCC_PLL1_SetVCOInputRange(LL_RCC_PLLINPUTRANGE_1_2);
  LL_RCC_PLL1_SetVCOOutputRange(LL_RCC_PLLVCORANGE_WIDE);
  LL_RCC_PLL1_SetM(25);
  LL_RCC_PLL1_SetN(480);
  LL_RCC_PLL1_SetP(2);
  LL_RCC_PLL1_SetQ(2);
  LL_RCC_PLL1_SetR(2);
  LL_RCC_PLL1_Enable();

   /* Wait till PLL is ready */
  while(LL_RCC_PLL1_IsReady() != 1)
  {
  }

   /* Intermediate AHB prescaler 2 when target frequency clock is higher than 80 MHz */
   LL_RCC_SetAHBPrescaler(LL_RCC_AHB_DIV_2);

  LL_RCC_SetSysClkSource(LL_RCC_SYS_CLKSOURCE_PLL1);

   /* Wait till System clock is ready */
  while(LL_RCC_GetSysClkSource() != LL_RCC_SYS_CLKSOURCE_STATUS_PLL1)
  {

  }
  LL_RCC_SetSysPrescaler(LL_RCC_SYSCLK_DIV_1);
  LL_RCC_SetAHBPrescaler(LL_RCC_AHB_DIV_2);
  LL_RCC_SetAPB1Prescaler(LL_RCC_APB1_DIV_1);
  LL_RCC_SetAPB2Prescaler(LL_RCC_APB2_DIV_1);
  LL_RCC_SetAPB3Prescaler(LL_RCC_APB3_DIV_1);
  LL_RCC_SetAPB4Prescaler(LL_RCC_APB4_DIV_1);

  LL_Init1msTick(240000000);

  LL_SetSystemCoreClock(240000000);
}

