/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */

//	if(LL_USART_IsEnabledIT_TXE(USART1) && LL_USART_IsActiveFlag_TXE(USART1)) // 如果启用了发送寄存器为空中断，并且发送寄存器为空
//	{
//		LL_USART_DisableIT_TXE(USART1); // 关闭串口发送寄存器为空中断
//	}

	if(LL_USART_IsActiveFlag_RXNE(USART1) && LL_USART_IsEnabledIT_RXNE(USART1)) // 如果有数据可读，并且启用了接收中断
	{
		uint8_t data = LL_USART_ReceiveData8(USART1); // 读取数据，读取后会自动清除RXNE标志位

		while(!LL_USART_IsActiveFlag_TXE(USART1)); // 等待发送数据数据寄存器为空

		LL_USART_TransmitData8(USART1, data); // 发送数据
	}

	if(LL_USART_IsEnabledIT_ERROR(USART1)) // 如果启用了错误中断
	{
		if(LL_USART_IsActiveFlag_FE(USART1)) // 如果是帧错误
		{
			LL_USART_ClearFlag_FE(USART1); // 清除帧错误标志
		}

		if(LL_USART_IsActiveFlag_NE(USART1)) // 如果是噪声错误
		{
			LL_USART_ClearFlag_NE(USART1); // 清除噪声错误标志
		}

		if(LL_USART_IsActiveFlag_ORE(USART1)) // 如果是溢出错误
		{
			LL_USART_ClearFlag_ORE(USART1); // 清除溢出错误标志
		}
	}

  /* USER CODE END USART1_IRQn 0 */
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

